ORG 00H

START:
RED:
MOV P1, #0FEH ; 11111110 in Hex (Red)
CALL DELAY5S;

YELLOW:
MOV P1, #7FH ; 01111111 in Hex (Yellow)
CALL DELAY1S;

GREEN:
MOV P1, #0DFH; 11011111 in Hex (Green)
CALL DELAY2S;

SJMP RED;

; 1s Delay
DELAY1S:
MOV R0,#03E8H; R0=1000
DELAY2: MOV R1,#3E8H; R1=1000
DELAY1: DJNZ R1,DELAY1;
		DJNZ R0,DELAY2;
RET

;2s Delay
DELAY2S:
MOV R0,#7D0H; R0=2000
DELAY6: MOV R1,#3E8H; R1=1000
DELAY5: DJNZ R1,DELAY5;
		DJNZ R0,DELAY6;
RET

;5s Delay
DELAY5S:
MOV R0,#1388H; R0=5000
DELAY4: MOV R1,#3E8H; R1=1000
DELAY3: DJNZ R1,DELAY3;
		DJNZ R0,DELAY4;
RET
END
